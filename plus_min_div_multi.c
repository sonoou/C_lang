#include<stdio.h>

int main(){
	int a=50, b=23, result;

	//addition
	result=a+b;
	printf("Addition of a & b %d\n",result);

	//subtraction
	result=a-b;
	printf("Subtraction of a & b %d\n",result);

	//multiplication
	result=a*b;
	printf("Multiplication of a & b %d\n",result);

	//division
	result=a/b;
	printf("Division of a & b %d\n",result);

	return 0;
}
